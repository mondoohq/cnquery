// routeFlagsMap maps route flag bits to their human-readable names
// Based on sys/route.h constants (RTF_*) common across platforms
var routeFlagsMap = map[int64]string{
	0x1:       "UP",        // RTF_UP - route is up
	0x2:       "GATEWAY",   // RTF_GATEWAY - route has a gateway
	0x4:       "HOST",      // RTF_HOST - host route (not a network route)
	0x8:       "REJECT",    // RTF_REJECT - reject route
	0x10:      "DYNAMIC",   // RTF_DYNAMIC - dynamically installed
	0x20:      "MODIFIED",  // RTF_MODIFIED - modified by redirect
	0x40:      "DONE",      // RTF_DONE - message confirmed
	0x80:      "MASK",      // RTF_MASK - subnet mask present
	0x100:     "CLONING",   // RTF_CLONING - generate new routes on use
	0x200:     "XRESOLVE",  // RTF_XRESOLVE - external daemon resolves name
	0x400:     "LLINFO",    // RTF_LLINFO - generated by link layer
	0x800:     "STATIC",    // RTF_STATIC - manually added
	0x1000:    "BLACKHOLE", // RTF_BLACKHOLE - just discard pkts
	0x2000:    "PROTO1",    // RTF_PROTO1 - protocol specific routing flag #1
	0x4000:    "PROTO2",    // RTF_PROTO2 - protocol specific routing flag #2
	0x40000:   "PROTO3",    // RTF_PROTO3 - protocol specific routing flag #3
	0x20000:   "WASCLONED", // RTF_WASCLONED - route generated through cloning
	0x100000:  "PRCLONING", // RTF_PRCLONING - protocol requires cloning
	0x1000000: "PINNED",    // RTF_PINNED - future use
	0x2000000: "LOCAL",     // RTF_LOCAL - route represents a local address
	0x4000000: "BROADCAST", // RTF_BROADCAST - route represents a broadcast address
	0x8000000: "MULTICAST", // RTF_MULTICAST - route represents a multicast address
}

// Routes returns the network routes of the system.
func Routes(conn shared.Connection, pf *inventory.Platform) ([]Route, error) {
	n := &neti{conn, pf}

	if pf.IsFamily(inventory.FAMILY_DARWIN) {
		return n.detectDarwinRoutes()
	}

	return nil, errors.New("your platform is not supported for the detection of network routes")
}

// detectDarwinRoutes detects network routes on macOS using golang.org/x/net/route
func (n *neti) detectDarwinRoutes() ([]Route, error) {
	// Get IPv4 routes
	ipv4Routes, err := n.fetchDarwinRoutes(unix.AF_INET)
	if err != nil {
		return nil, errors.Wrap(err, "failed to get IPv4 routes")
	}

	// Get IPv6 routes
	ipv6Routes, err := n.fetchDarwinRoutes(unix.AF_INET6)
	if err != nil {
		// IPv6 routes are optional, log but don't fail
		log.Debug().Err(err).Msg("failed to get IPv6 routes")
	}
	routes := append(ipv4Routes, ipv6Routes...)

	return routes, nil
}

// fetchDarwinRoutes fetches routes for a specific address family
func (n *neti) fetchDarwinRoutes(af int) ([]Route, error) {
	// Fetch routing information base
	rib, err := route.FetchRIB(af, route.RIBTypeRoute, 0)
	if err != nil {
		return nil, errors.Wrap(err, "failed to fetch RIB")
	}

	// Parse the RIB
	messages, err := route.ParseRIB(route.RIBTypeRoute, rib)
	if err != nil {
		return nil, errors.Wrap(err, "failed to parse RIB")
	}

	// Get interface map to resolve interface indices to names
	interfaceMap, err := n.getDarwinInterfaceMap()
	if err != nil {
		log.Debug().Err(err).Msg("failed to get interface map, using indices")
		interfaceMap = make(map[int]string)
	}

	var routes []Route
	for _, msg := range messages {
		routeMsg, ok := msg.(*route.RouteMessage)
		if !ok {
			continue
		}

		// Extract route information
		dest, gateway, iface, err := n.parseRouteMessage(routeMsg, interfaceMap)
		if err != nil {
			log.Debug().Err(err).Msg("failed to parse route message")
			continue
		}

		if dest == "" {
			continue
		}

		// Filter out IPv6 multicast routes (ff00::/8) to match osquery results
		if strings.Contains(dest, ":") && strings.HasPrefix(dest, "ff") {
			continue
		}

		routes = append(routes, Route{
			Destination: dest,
			Gateway:     gateway,
			Flags:       parseRouteFlags(int64(routeMsg.Flags)),
			Interface:   iface,
		})
	}

	return routes, nil
}

// getDarwinInterfaceMap creates a map of interface index to interface name
func (n *neti) getDarwinInterfaceMap() (map[int]string, error) {
	interfaceMap := make(map[int]string)

	// Fetch interface list
	rib, err := route.FetchRIB(unix.AF_UNSPEC, route.RIBTypeInterface, 0)
	if err != nil {
		return nil, err
	}

	messages, err := route.ParseRIB(route.RIBTypeInterface, rib)
	if err != nil {
		return nil, err
	}

	for _, msg := range messages {
		if ifMsg, ok := msg.(*route.InterfaceMessage); ok {
			interfaceMap[ifMsg.Index] = ifMsg.Name
		}
	}

	return interfaceMap, nil
}

// parseRouteMessage extracts destination, gateway, and interface from a RouteMessage
// RouteMessage.Addrs contains addresses in specific order on macOS:
// Index 0: Destination (Inet4Addr/Inet6Addr)
// Index 1: Gateway (Inet4Addr/Inet6Addr or LinkAddr)
// Index 2: Netmask (Inet4Addr/Inet6Addr, if present)
// Index 3: Interface (LinkAddr, if present)
func (n *neti) parseRouteMessage(routeMsg *route.RouteMessage, interfaceMap map[int]string) (dest, gateway, iface string, err error) {
	// Get destination (index 0)
	if len(routeMsg.Addrs) > 0 && routeMsg.Addrs[0] != nil {
		dest = n.addrToString(routeMsg.Addrs[0], interfaceMap)
	}

	// Get gateway (index 1) - can be Inet4Addr, Inet6Addr, or LinkAddr
	if len(routeMsg.Addrs) > 1 && routeMsg.Addrs[1] != nil {
		switch a := routeMsg.Addrs[1].(type) {
		case *route.LinkAddr:
			gateway = fmt.Sprintf("link#%d", a.Index)
		default:
			gateway = n.addrToString(routeMsg.Addrs[1], interfaceMap)
		}
	}

	// Get netmask (index 2) and convert to CIDR if present
	if len(routeMsg.Addrs) > 2 && routeMsg.Addrs[2] != nil && dest != "" {
		var maskBytes []byte
		switch a := routeMsg.Addrs[2].(type) {
		case *route.Inet4Addr:
			maskBytes = a.IP[:]
		case *route.Inet6Addr:
			maskBytes = a.IP[:]
		}
		if maskBytes != nil {
			ones, bits := net.IPMask(maskBytes).Size()
			if bits > 0 {
				dest = fmt.Sprintf("%s/%d", dest, ones)
			}
		}
	}

	if routeMsg.Index > 0 {
		if name, ok := interfaceMap[routeMsg.Index]; ok {
			iface = name
		} else {
			iface = fmt.Sprintf("%d", routeMsg.Index)
		}
	} else if len(routeMsg.Addrs) > 3 {
		if linkAddr, ok := routeMsg.Addrs[3].(*route.LinkAddr); ok && linkAddr.Name != "" {
			iface = linkAddr.Name
		}
	}

	return dest, gateway, iface, nil
}

// addrToString converts a route.Addr to a string IP address
// For IPv6 addresses with zone IDs, converts the zone ID to interface name to match osquery format
func (n *neti) addrToString(addr route.Addr, interfaceMap map[int]string) string {
	switch a := addr.(type) {
	case *route.Inet4Addr:
		return net.IPv4(a.IP[0], a.IP[1], a.IP[2], a.IP[3]).String()
	case *route.Inet6Addr:
		ip := make(net.IP, 16)
		copy(ip, a.IP[:])
		if a.ZoneID > 0 {
			// Convert zone ID to interface name to match osquery format (e.g., %16 -> %utun1)
			if ifaceName, ok := interfaceMap[a.ZoneID]; ok {
				return fmt.Sprintf("%s%%%s", ip.String(), ifaceName)
			}
			// Fallback to zone ID number if interface name not found
			return fmt.Sprintf("%s%%%d", ip.String(), a.ZoneID)
		}
		return ip.String()
	default:
		return ""
	}
}

// parseRouteFlags converts route flags integer to an array of flag strings
func parseRouteFlags(flags int64) []string {
	return parseFlags(flags, routeFlagsMap)
}

func parseFlags(flags int64, flagsMap map[int64]string) []string {
	var flagStrings []string
	for bit, name := range flagsMap {
		if flags&bit != 0 {
			flagStrings = append(flagStrings, name)
		}
	}
	return flagStrings
}
